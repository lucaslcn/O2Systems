    public void limpar() {
        this.remedio = new Remedio();
        TF_NomeRemedio.setText("");
        TF_NomeRemedio.requestFocus();
    }

    public void popular() {
        String nomeRemedio = TF_NomeRemedio.getText();
        if (Gema.vazio(nomeRemedio, 2)) {
            this.remedio.setNomeRemedio(nomeRemedio);
        }
    }
